FROM book;

Написать SQL запрос, который из таблицы book выбирает названия, авторов, количества и вычисляет новые цены книг. Столбец с новой ценой назвать new_price, цену округлить до 2-х знаков после запятой.
SELECT title, author, amount,
ROUND(0.7 * price, 2) AS new_price
FROM book;

Написать запрос, куда включить автора, название книги и новую цену, последний столбец назвать new_price. Значение округлить до двух знаков после запятой.
SELECT author, title,
ROUND(IF(author='Булгаков М.А.',price*1.1,IF(author='Есенин С.А.',price*1.05,price*1)),2) AS new_price
